
# Convention all lower case with underscore for word separator
my_var = 22
my_string = 'hello'

# Can have numbers
my_var9 = 17

# A number can't start a variable however
# 9var = 8

# Can use Caps, but ALL_CAPS by convention is a constant
MY_ROUTER = '10.1.1.1'

# Can mix upper and lower if you want (but follow conventions)
myRouter = '10.1.1.2'
